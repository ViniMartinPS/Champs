package modelo;

public class CampeonatoMataMata {

	private int idCampeonato;
	private int idMataMata;
	private boolean flagSorteioProximaFase;
	private boolean flagIdaVolta;
	private boolean flagPropriosFora;
	
	public int getIdCampeonato() {
		return idCampeonato;
	}
	public void setIdCampeonato(int idCampeonato) {
		this.idCampeonato = idCampeonato;
	}
	public int getIdMataMata() {
		return idMataMata;
	}
	public void setIdMataMata(int idMataMata) {
		this.idMataMata = idMataMata;
	}
	public boolean isFlagSorteioProximaFase() {
		return flagSorteioProximaFase;
	}
	public void setFlagSorteioProximaFase(boolean flagSorteioProximaFase) {
		this.flagSorteioProximaFase = flagSorteioProximaFase;
	}
	public boolean isFlagIdaVolta() {
		return flagIdaVolta;
	}
	public void setFlagIdaVolta(boolean flagIdaVolta) {
		this.flagIdaVolta = flagIdaVolta;
	}
	public boolean isFlagPropriosFora() {
		return flagPropriosFora;
	}
	public void setFlagPropriosFora(boolean flagPropriosFora) {
		this.flagPropriosFora = flagPropriosFora;
	}
	
	
	
}
