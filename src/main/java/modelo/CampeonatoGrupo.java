package modelo;

public class CampeonatoGrupo {

	private int idCampeonato;
	private int idGrupo;
	private int quantidadeTurnos;
	private boolean flagSorteioProximaFase;
	private boolean flagIdaVolta;
	private boolean flagPropriosFora;
	private int quantidadeClassificados;
	private int quantidadeGrupos;
	
	public int getIdCampeonato() {
		return idCampeonato;
	}
	public void setIdCampeonato(int idCampeonato) {
		this.idCampeonato = idCampeonato;
	}
	public int getIdGrupo() {
		return idGrupo;
	}
	public void setIdGrupo(int idGrupo) {
		this.idGrupo = idGrupo;
	}
	public int getQuantidadeTurnos() {
		return quantidadeTurnos;
	}
	public void setQuantidadeTurnos(int quantidadeTurnos) {
		this.quantidadeTurnos = quantidadeTurnos;
	}
	public boolean isFlagSorteioProximaFase() {
		return flagSorteioProximaFase;
	}
	public void setFlagSorteioProximaFase(boolean flagSorteioProximaFase) {
		this.flagSorteioProximaFase = flagSorteioProximaFase;
	}
	public boolean isFlagIdaVolta() {
		return flagIdaVolta;
	}
	public void setFlagIdaVolta(boolean flagIdaVolta) {
		this.flagIdaVolta = flagIdaVolta;
	}
	public boolean isFlagPropriosFora() {
		return flagPropriosFora;
	}
	public void setFlagPropriosFora(boolean flagPropriosFora) {
		this.flagPropriosFora = flagPropriosFora;
	}
	public int getQuantidadeClassificados() {
		return quantidadeClassificados;
	}
	public void setQuantidadeClassificados(int quantidadeClassificados) {
		this.quantidadeClassificados = quantidadeClassificados;
	}
	public int getQuantidadeGrupos() {
		return quantidadeGrupos;
	}
	public void setQuantidadeGrupos(int quantidadeGrupos) {
		this.quantidadeGrupos = quantidadeGrupos;
	}
	
	
	
}
