package modelo;

public class Desempate {
	
	private int idDesempate;
	private int nomeDesempate;
	private int prioridade;
	public int getIdDesempate() {
		return idDesempate;
	}
	public void setIdDesempate(int idDesempate) {
		this.idDesempate = idDesempate;
	}
	public int getNomeDesempate() {
		return nomeDesempate;
	}
	public void setNomeDesempate(int nomeDesempate) {
		this.nomeDesempate = nomeDesempate;
	}
	public int getPrioridade() {
		return prioridade;
	}
	public void setPrioridade(int prioridade) {
		this.prioridade = prioridade;
	}
	
	

}
